
import { useMemo } from "react";

const CONTINENT_BITMAP = [
  "............................................................",
  "..........#####.....##############......####................",
  ".......##########.################.......######............",
  "......##############.###############.....##########.........",
  "....##################.#############......##########........",
  ".....################...##############.....#########.......#",
  "......#############......#############......#######........#",
  ".......#########..........############.......#####..........",
  "........#######............##########..........###..........",
  ".........#####..............########............#...........",
  "..........###...............#######.........................",
  "..........###................######..........###............",
  "..........###.................####...........####...........",
  "..........###..................###...........####...........",
  "..........####.................##.............###...........",
  "...........####................#...............#............",
  "...........####.............................................",
  "............###..............................................",
  ".............##..............................................",
  "..............#..............................................",
  ".............................................................",
  ".............................................................",
];

export function WorldMap({ className = "" }: { className?: string }) {
  const dots = useMemo(() => {
    const out: { x: number; y: number; r: number; o: number }[] = [];
    const cols = CONTINENT_BITMAP[0].length;
    const rows = CONTINENT_BITMAP.length;
    for (let y = 0; y < rows; y++) {
      for (let x = 0; x < cols; x++) {
        if (CONTINENT_BITMAP[y][x] === "#") {
          const seed = (x * 13 + y * 7) % 9;
          out.push({
            x: x * 16 + 8 + (seed % 3) - 1,
            y: y * 16 + 8 + ((seed * 2) % 3) - 1,
            r: 1.4 + (seed % 3) * 0.25,
            o: 0.32 + (seed % 5) * 0.07,
          });
        }
      }
    }
    return out;
  }, []);

  return (
    <svg
      className={className}
      viewBox={`0 0 ${60 * 16} ${22 * 16}`}
      preserveAspectRatio="xMidYMid slice"
      aria-hidden
    >
      <defs>
        <radialGradient id="mapfade" cx="50%" cy="50%" r="60%">
          <stop offset="0%" stopColor="#a78bfa" stopOpacity="0.55" />
          <stop offset="60%" stopColor="#a78bfa" stopOpacity="0.18" />
          <stop offset="100%" stopColor="#a78bfa" stopOpacity="0" />
        </radialGradient>
        <mask id="fademask">
          <rect width="100%" height="100%" fill="url(#mapfade)" />
        </mask>
      </defs>
      <g mask="url(#fademask)">
        {dots.map((d, i) => (
          <circle key={i} cx={d.x} cy={d.y} r={d.r} fill="#c4b5fd" opacity={d.o} />
        ))}
      </g>
    </svg>
  );
}
